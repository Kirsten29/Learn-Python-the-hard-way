# + plus does addition
# - minus  does subtract
# / slash does divide
# * asterisk does multiply
# % percent/percentage is the 100th part of a given number
# < less-than means less than number given
# > greater-than means more than number given
# <= less-than-equal means same or less than number given
# >= greater-than-equal means same or more than number given

